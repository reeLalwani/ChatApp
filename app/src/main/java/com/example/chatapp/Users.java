package com.example.chatapp;

public class Users {
    public String name;
    public String status;
    public String image;//this names must match with keys in database

    public Users(){

    }//We also need an empty constructor or app may crash
    public Users(String name, String status, String image) {
        this.name = name;
        this.status = status;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
